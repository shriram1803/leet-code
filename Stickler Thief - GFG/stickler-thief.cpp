class Solution
{
    public:
    //Function to find the maximum money the thief can get.
    int FindMaxSum(int arr[], int n)
    {
        // Your code here
        int pre2 = 0, pre1 = arr[0], curr;
        for(int i = 1; i < n; ++i) {
            curr = pre1;
            curr = max(pre1, arr[i] + pre2);
            pre2 = pre1, pre1 = curr;
        }
        return pre1;
    }
};

